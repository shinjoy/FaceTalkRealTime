package com.nomadsoft.chat.utility;

import java.text.SimpleDateFormat;

public class ChatDate {
	static SimpleDateFormat formatterServerDate = new SimpleDateFormat("yyyyMMddHHmmssSSS");
	static SimpleDateFormat formatterReqDate = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
	static SimpleDateFormat formatterReqDate2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
	
	
	public static SimpleDateFormat getFormatterServerDate() {
		return formatterServerDate;
	}
	
	public static SimpleDateFormat getFormatterReqDate() {
		return formatterReqDate;
	}
	
	public static SimpleDateFormat getFormatterReqDate2() {
		return formatterReqDate2;
	}
}
